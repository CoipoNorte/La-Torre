export function getFloorText(floor: number): string {
  let key: string;
  if (floor >= 100) key = '100';
  else if (floor >= 81) key = '81-99';
  else if (floor >= 61) key = '61-80';
  else if (floor >= 45) key = '45-60';
  else if (floor >= 31) key = '31-44';
  else if (floor >= 16) key = '16-30';
  else key = '1-15';
  const texts = FLOOR_TEXTS[key];
  return texts[Math.floor(Math.random() * texts.length)];
}

export function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}
